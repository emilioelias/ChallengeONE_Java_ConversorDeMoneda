    private void txtValorTemperaturaIngresadoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorTemperaturaIngresadoKeyTyped

        //Validacion del campo para ingreso de datos solo decimal con "."
        char caracter = evt.getKeyChar();

        if ((caracter < '0' || caracter > '9')
                && (caracter != KeyEvent.VK_BACKSPACE)
                && (caracter != '-' || txtValorTemperaturaIngresado.getText().contains("-"))
                && (caracter != '.' || txtValorTemperaturaIngresado.getText().contains("."))) {
            evt.consume();
        }
    }//GEN-LAST:event_txtValorTemperaturaIngresadoKeyTyped

