    private void btnCalularConversionTemperaturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalularConversionTemperaturaActionPerformed

        double dblValorTemperaturaIngresado = Double.parseDouble(txtValorTemperaturaIngresado.getText());
        double dblResultado = 0;
        String strResultado;
        String temperaturaSeleccionada1 = (String) cbxSelectorTemperatura1.getSelectedItem();
        String temperaturaSeleccionada2 = (String) cbxSelectorTemperatura2.getSelectedItem();
        

        if (temperaturaSeleccionada1.equals("Seleccionar unidad...")) {
            JOptionPane.showMessageDialog(null, "Seleccionar unidad");
        } else if (temperaturaSeleccionada2.equals("Seleccionar unidad...")) {
            JOptionPane.showMessageDialog(null, "Seleccionar unidad");
        } else if (temperaturaSeleccionada1.equals("Grado Celsius") && temperaturaSeleccionada2.equals("Grado Fahrenheit")) {
            dblResultado = dblValorTemperaturaIngresado * 9 / 5 + 32;
            System.out.println(dblResultado);
        } else if (temperaturaSeleccionada1.equals("Grado Celsius") && temperaturaSeleccionada2.equals("Kelvin")) {
            dblResultado = dblValorTemperaturaIngresado + 273;
            System.out.println(dblResultado);
        } else if (temperaturaSeleccionada1.equals("Grado Fahrenheit") && temperaturaSeleccionada2.equals("Grado Celsius")) {
            dblResultado = (dblValorTemperaturaIngresado - 32) * 1.8;
            System.out.println(dblResultado);
        } else if (temperaturaSeleccionada1.equals("Grado Fahrenheit") && temperaturaSeleccionada2.equals("Kelvin")) {
            dblResultado = (1.8 * (dblValorTemperaturaIngresado - 32)) + 273.15;
            System.out.println(dblResultado);
        } else if (temperaturaSeleccionada1.equals("Kelvin") && temperaturaSeleccionada2.equals("Grado Celsius")) {
            dblResultado = dblValorTemperaturaIngresado - 273.15;
            System.out.println(dblResultado);
        } else if (temperaturaSeleccionada1.equals("Kelvin") && temperaturaSeleccionada2.equals("Grado Fahrenheit")) {
            dblResultado = ((dblValorTemperaturaIngresado - 273.15) * (9 / 5)) + 32;
            System.out.println(dblResultado);
        } else {
            dblResultado = dblValorTemperaturaIngresado;
            System.out.println(dblResultado);
        }

        strResultado = Double.toString(dblResultado);
        lblResultado.setText(dblValorTemperaturaIngresado + " " + temperaturaSeleccionada1 + " equivale a " + strResultado + " " + temperaturaSeleccionada2);

    }//GEN-LAST:event_btnCalularConversionTemperaturaActionPerformed

